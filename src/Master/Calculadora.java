package Master;

public class Calculadora {

    //Método de Soma
    public int somar(int a, int b) {
        return a + b;
    }

    //Método de Subtração
    public int subtrair(int a, int b){
        return a - b;
    }

    //Método de Divição
    public int divicao(int a, int b){
        return a / b;
    }

    //Método de Multiplicação
    public int multiplicacao(int a, int b){
        return a * b;
    }
}
